    public String pilihJk()
    {
        String jk = "";
        if(radioL.isSelected()){
            jk = "L";
        } else if(radioP.isSelected()){
            jk = "P";
        } 
        return jk;
    }
    
    public void fillForm(Customer c){
        txtKdCst.setText(c.getKdCustomer());
        txtNamaCst.setText(c.getNamaCst());
        if("L".equals(c.getJkCst())){
            radioL.setSelected(true);
        } else if("P".equals(c.getJkCst())){
            radioP.setSelected(true);
        }
        txtAlamatCst.setText(c.getAlamatCst());
        txtNotelpCst.setText(c.getNotelpCst());
    }
    
