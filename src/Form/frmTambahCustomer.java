    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        String kdCustomer = txtKdCst.getText();
        String namaCustomer = txtNamaCst.getText();
        String alamatCustomer = txtAlamatCst.getText();
        String notelpCustomer = txtNotelpCst.getText();
        
        if("".equals(kdCustomer) || "Nama Customer".equals(namaCustomer) || jkGroup.getSelection() == null || "Alamat Customer".equals(alamatCustomer) || "No. Telp".equals(notelpCustomer))
        {
            JOptionPane.showMessageDialog(null, "Masukan harus terisi semua!");
        } else {
            String jkCst = pilihJk();
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Apakah anda yakin ingin menyimpan data?","Konfirmasi", dialogButton);
            if(dialogResult == JOptionPane.YES_OPTION)
            {
                cst.tambahCustomer(new Customer(kdCustomer, namaCustomer, jkCst, alamatCustomer, notelpCustomer));
                JOptionPane.showMessageDialog(null, "Data Customer Berhasil Disimpan.");
                setVisible(false);  
            }         
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

