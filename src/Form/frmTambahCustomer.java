//    NOMOR OTOMATIS
    public String idOtomatis() {
        Connection conn = null;
        Statement stmt = null;
        String kdCustomerNew = "";
         try {
            conn = koneksi.getKoneksi();
            String sql = "SELECT * FROM customer ORDER BY kdCustomer DESC";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            if (rs.next()) {
                String kdCustomer = rs.getString("kdCustomer").substring(1);
                String AN = "" + (Integer.parseInt(kdCustomer) + 1);
                String Nol = "";

                if(AN.length()==1)
                    {Nol = "00";
                }else if(AN.length()==2){
                    Nol = "0";
                }else if(AN.length()==3){
                    Nol = "";
                }
                kdCustomerNew = "C"+Nol+AN;
            } else {
                kdCustomerNew = "C001";
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        finally {
            try {
                stmt.close();
            } catch(Exception e) {}
            try {
                conn.close();
            } catch(Exception e) {}
            
        }
        return kdCustomerNew;
    }
    
